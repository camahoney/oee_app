def get_seed_rates():
    rates = []
    # Read strict CSV from string
    reader = csv.DictReader(io.StringIO(SEED_CSV_DATA))
    
    for row in reader:
        # Handle empty/missing values safely
        ideal_units = row.get("StandardRatePPH")
        ideal_seconds = row.get("IdealCycleTimeSeconds")
        
        rates.append(RateEntry(
            part_number=row["PartNumber"],
            machine=row["Workstation"] or "Unknown",
            operator="Any",
            job="Seed Data",
            ideal_units_per_hour=float(ideal_units) if ideal_units else None,
            ideal_cycle_time_seconds=float(ideal_seconds) if ideal_seconds else None,
            start_date=date.today(),
            active=True,
            notes="Hardcoded Seed Data"
        ))
    return rates
